/** 首章生成规则 */
export const FIRST_CHAPTER_RULES = `【首章生成规则】
1. 注入设定顺序：世界观 → 人物库 → 关系网 → 阵营 → 本章大纲
2. 人物库按主角 → 配角 → 反派 → NPC 顺序展示
3. 关系网展示人物之间的亲缘 / 师徒 / 敌对 / 同盟等关系
4. 阵营标注正派 / 反派 / 中立 / 灰色等立场
5. 严格按本章大纲扩写，不得偏离世界观设定
6. 字数控制在 2000~2500 字
7. 使用 HTML 标签：<h1> 章节标题、<p> 段落、<blockquote> 对话或独白
8. 禁止使用 markdown 语法（# / ** / > 等）
9. 禁止出现"剧本式"括号描写（如（他心想...））`;

/** 续章生成规则 */
export const CONTINUATION_CHAPTER_RULES = `【续章生成规则】
1. 注入设定顺序：世界观 → 人物库 → 关系网 → 阵营 → 本章大纲
2. 人物库按主角 → 配角 → 反派 → NPC 顺序展示
3. 关系网展示人物之间的亲缘 / 师徒 / 敌对 / 同盟等关系
4. 阵营标注正派 / 反派 / 中立 / 灰色等立场
5. 严格按本章大纲扩写，不得偏离世界观设定
6. **关键**：必须承接"上一章末尾 500 字原文"的场景、人物情绪、对话或动作
7. 不得复述前文内容，不得与前一章末尾矛盾
8. 人物情绪、场景、地点必须自然延续
9. 如有未完成的对话或动作，必须承接处理
10. 字数控制在 2000~2500 字
11. 使用 HTML 标签：<h1> 章节标题、<p> 段落、<blockquote> 对话或独白
12. 禁止使用 markdown 语法（# / ** / > 等）
13. 禁止出现"剧本式"括号描写（如（他心想...））`;

/** 通用规则（适用所有章节） */
export const COMMON_RULES = `【通用规则】
- 输出语言：中文（除非用户指定其他语言）
- 输出格式：HTML 片段，标签限定为 <h1>/<h2>/<h3>/<p>/<blockquote>/<strong>/<em>/<br>
- 禁用：\`\`\` 代码块、Markdown 标题 #、剧本式括号描写
- 字数控制：2000~2500 字（允许 ±200 字浮动）
- 保持叙事视角一致（除非大纲明确要求切换视角）
- 人物行为必须符合其性格设定和关系网中的定位
- 不写"好的/以下是/Chapter X"等多余套话，直接输出正文`;

/** 完整规则（首章） */
export const FIRST_CHAPTER_FULL_RULES =
  FIRST_CHAPTER_RULES + '\n\n' + COMMON_RULES;

/** 完整规则（续章） */
export const CONTINUATION_CHAPTER_FULL_RULES =
  CONTINUATION_CHAPTER_RULES + '\n\n' + COMMON_RULES;

/**
 * 获取章节生成规则
 * @param isFirstChapter - 是否为首章（第1章）
 */
export function getRules(isFirstChapter: boolean): string {
  return isFirstChapter ? FIRST_CHAPTER_FULL_RULES : CONTINUATION_CHAPTER_FULL_RULES;
}

